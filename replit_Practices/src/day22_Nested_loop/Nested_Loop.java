package day22_Nested_loop;

public class Nested_Loop {
    public static void main(String[] args) {

            int j = 0;
            while (j <= 10){
                System.out.print("*");
                j++;
                System.out.println();
                for (int i = 1; i <= 5;i++){
                    System.out.print("*");

            }
        }

    }
}
