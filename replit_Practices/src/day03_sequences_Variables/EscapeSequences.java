package day03_sequences_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone \n\tMy name is Gerald");
        System.out.println("\n\nI'm from Kazakhstan");
        System.out.println("\n\n\t\tI love programming");

        System.out.println("My name is \"Beslan\"");
    }

}
