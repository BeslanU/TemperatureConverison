package day04_Variable;

public class Variables2 {

    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 2467;

        System.out.println(ch2);

        char c1 = 141;
        char c2 = 144;

        System.out.println(c1 + c2);

        double d = c1 + c2;
        System.out.println(d);

        char c4 = 'A' + 'G';
        System.out.println(c4);
    }
}
